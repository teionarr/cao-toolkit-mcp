#!/usr/bin/env node
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, "..");

const files = {
  SKILL_TEXT: join(root, "content", "SKILL_toolkit_adoption.md"),
  REFERENCE_TEXT: join(root, "content", "REFERENCE_toolkit_adoption.md"),
  MAIN_ARTICLE_TEXT: join(root, "content", "MAIN_ARTICLE.md"),
  KPI_TRACKER_TEMPLATE: join(root, "content", "templates", "kpi-tracker.md"),
};

const loaded = {};
for (const [name, path] of Object.entries(files)) {
  if (!existsSync(path)) {
    console.error(`Missing ${path}`);
    process.exit(1);
  }
  loaded[name] = readFileSync(path, "utf8");
}

const lines = ["// AUTO-GENERATED by scripts/sync-content.mjs. Do not edit by hand.", ""];
for (const [name, text] of Object.entries(loaded)) {
  lines.push(`export const ${name}: string = ${JSON.stringify(text)};`);
  lines.push("");
}
writeFileSync(join(root, "src", "content.ts"), lines.join("\n"), "utf8");

const total = Object.values(loaded).reduce((s, t) => s + t.length, 0);
console.log(`Wrote src/content.ts (${Object.keys(loaded).length} files, ${total} chars total).`);
